#!/usr/bin/env python3
"""
E06 — Engine ensemble combiner.

Given the stored predictions from E00 / E01 / E02 / E03 / E04 / E05 / E07 / E08
against the samples-mix/public dataset, build an ensemble prediction per
sample using several strategies, evaluate each strategy against the current
local GT (strict-equality, one-of array aware), and report which strategy
wins.

Strategies explored:

  S1  — "per-category best engine": picks the value from the per-category
        best engine (E01 for sin, E03 for checkboxes, E05 for date/name/
        freeform_text, E07 for signature, E08 for phone/income_amounts)
        with no fallback.

  S2  — "per-category best, majority-vote fallback": same as S1, but when
        the chosen engine returned null-like, fall back to the value that
        the most engines (any of the 5) return.

  S3  — "majority vote ≥ 3, else per-category best": for each field, pick
        the value that ≥3 engines agree on. If no such value exists,
        fall back to the per-category best engine.

  S4  — "weighted majority (per-engine per-category weight)": each engine
        gets a weight equal to its per-category accuracy. The output is the
        value with the highest sum of weights.

  S5  — "S4 + null-preferred-when-best-engine-says-null": same as S4 but if
        the per-category best engine returns null AND any other engine
        agrees, prefer null. This avoids over-extraction when the best
        engine sees nothing on the form.

The ensemble metrics, per-category accuracy, and a per-strategy comparison
table are written under `experiments/results/06-engine-ensemble/`.
"""


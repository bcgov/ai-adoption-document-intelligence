def classify_field(field: str) -> str:
    if field.startswith("checkbox_"):
        return "checkboxes"
    if field in {"sin", "spouse_sin"}:
        return "sin"
    if field in {"date", "spouse_date"}:
        return "date"
    if field in {"phone", "spouse_phone"}:
        return "phone"
    if field in {"name", "spouse_name"}:
        return "name"
    if field in {"signature", "spouse_signature"}:
        return "signature"
    if field == "explain_changes":
        return "freeform_text"
    return "income_amounts"


def parse_calendar_parts(s: str):
    """Mirror of `parseToCalendarParts` in apps/temporal/src/form-field-normalization.ts."""
    if s is None:
        return None
    s = str(s).strip()
    if not s:
        return None
    month_names = {
        "jan": 1, "january": 1, "feb": 2, "february": 2, "mar": 3, "march": 3,
        "apr": 4, "april": 4, "may": 5, "jun": 6, "june": 6,
        "jul": 7, "july": 7, "aug": 8, "august": 8, "sep": 9, "sept": 9, "september": 9,
        "oct": 10, "october": 10, "nov": 11, "november": 11, "dec": 12, "december": 12,
    }
    # ISO: YYYY-MM-DD
    m = re.match(r"^(\d{4})-(\d{1,2})-(\d{1,2})$", s)
    if m:
        return (int(m.group(1)), int(m.group(2)), int(m.group(3)))
    # YYYY-MMM-DD
    m = re.match(r"^(\d{4})-([A-Za-z]+)-(\d{1,2})$", s)
    if m and m.group(2).lower() in month_names:
        return (int(m.group(1)), month_names[m.group(2).lower()], int(m.group(3)))
    # YYYY/MM/DD or YYYY/M/D
    m = re.match(r"^(\d{4})/(\d{1,2})/(\d{1,2})$", s)
    if m:
        return (int(m.group(1)), int(m.group(2)), int(m.group(3)))
    # Month DD, YYYY  or  Month DD,YYYY
    m = re.match(r"^([A-Za-z]+)\s+(\d{1,2}),\s*(\d{4})$", s)
    if m and m.group(1).lower() in month_names:
        return (int(m.group(3)), month_names[m.group(1).lower()], int(m.group(2)))
    # Mon D/YY (e.g. "Mar 2/26")
    m = re.match(r"^([A-Za-z]+)\s+(\d{1,2})/(\d{2,4})$", s)
    if m and m.group(1).lower() in month_names:
        y = int(m.group(3))
        if y < 100:
            y += 2000
        return (y, month_names[m.group(1).lower()], int(m.group(2)))
    return None


def is_null_like_scalar(v) -> bool:
    return v is None or v == "" or (isinstance(v, str) and v.strip().lower() == "null")


def alternatives_of(expected):
    return expected if isinstance(expected, list) else [expected]


def is_null_like(v) -> bool:
    if isinstance(v, list):
        return len(v) == 0 or all(is_null_like_scalar(x) for x in v)
    return is_null_like_scalar(v)


# Mirror of apps/temporal/src/evaluators/schema-aware-evaluator.ts
WILDCARD_SENTINELS = {":garbled:"}
PRESENCE_ONLY_FIELDS = {"signature", "spouse_signature"}


def is_wildcard_expected(expected) -> bool:
    for a in alternatives_of(expected):
        if isinstance(a, str) and a in WILDCARD_SENTINELS:
            return True
    return False


def field_matches(predicted, expected, field: str | None = None) -> bool:
    """Mirror of the TS schema-aware-evaluator compareField rule with one-of
    array support: wildcard `:garbled:` GT, presence-only signatures, and
    exact-or-date-equivalent matching otherwise.
    """
    # Wildcard GT — any prediction matches when an alternate is `:garbled:`.
    if is_wildcard_expected(expected):
        return True
    if is_null_like(predicted):
        # Predicted is null. Matches if any GT alternate is null-like.
        return any(is_null_like_scalar(x) for x in alternatives_of(expected))
    # Presence-only: match iff GT has at least one non-null alternate.
    if field is not None and field in PRESENCE_ONLY_FIELDS:
        return any(not is_null_like_scalar(a) for a in alternatives_of(expected))
    # Try date match first if both sides parse as calendar dates.
    pdate = parse_calendar_parts(str(predicted))
    for alt in alternatives_of(expected):
        if pdate is not None:
            edate = parse_calendar_parts(str(alt))
            if edate is not None and pdate == edate:
                return True
        if str(predicted) == str(alt):
            return True
    return False


def load_per_category_weights() -> dict:
    weights = {}
    with open(PER_CATEGORY_WEIGHTS_CSV) as f:
        reader = csv.DictReader(f)
        for row in reader:
            cat = row["category"]
            weights[cat] = {tag: float(row[tag]) for tag, _ in ENGINES}
    return weights


def load_predictions(slug: str) -> dict[str, dict]:
    path = RESULTS_DIR / slug / "benchmark-run.json"
    with open(path) as f:
        d = json.load(f)
    preds = {}
    for s in d["perSampleResults"]:
        if s.get("sampleId") == "manifest":
            continue
        if s.get("prediction") is not None:
            preds[s["sampleId"]] = s["prediction"]
    return preds


def load_gt(sample_id: str) -> dict:
    with open(GT_DIR / f"{sample_id}.json") as f:
        return json.load(f)


def canonical_for_vote(value) -> str:
    """Normalize value for majority-vote bucketing. Null-like collapses to
    the sentinel "__NULL__"; dates collapse to their calendar tuple; other
    values pass through as their str() form."""
    if is_null_like(value):
        return "__NULL__"
    pdate = parse_calendar_parts(str(value))
    if pdate is not None:
        return f"DATE:{pdate[0]:04d}-{pdate[1]:02d}-{pdate[2]:02d}"
    return f"VAL:{str(value).strip().lower()}"


def majority_value(values_by_engine: dict, threshold: int = 3) -> tuple[str, list]:
    """Returns (status, candidate_values) where status is one of:
       "majority": at least `threshold` engines agree on a non-null value;
                   candidate_values = [those values, taking the first].
       "all_null": every engine says null-like.
       "no_majority": no value has ≥threshold votes."""
    canon = {tag: canonical_for_vote(v) for tag, v in values_by_engine.items()}
    counts = Counter(canon.values())
    # Pick the most common canon
    if not counts:
        return ("no_majority", [])
    most_common, ct = counts.most_common(1)[0]
    if most_common == "__NULL__" and ct >= threshold:
        return ("all_null", [None])
    if ct >= threshold and most_common != "__NULL__":
        # Find a representative value from the engines that voted this way.
        for tag, c in canon.items():
            if c == most_common:
                return ("majority", [values_by_engine[tag]])
    return ("no_majority", [])


def weighted_value(values_by_engine: dict, category: str, weights: dict) -> object:
    """Return the value with the largest sum of per-engine per-category weights."""
    w_for_cat = weights.get(category, {tag: 1.0 for tag, _ in ENGINES})
    canon = {tag: canonical_for_vote(v) for tag, v in values_by_engine.items()}
    scores = defaultdict(float)
    rep = {}
    for tag, c in canon.items():
        scores[c] += w_for_cat.get(tag, 0.0)
        if c not in rep:
            rep[c] = values_by_engine[tag]
    best = max(scores.items(), key=lambda x: x[1])
    return None if best[0] == "__NULL__" else rep[best[0]]


def strategy_s1(field: str, vals: dict, weights: dict):
    cat = classify_field(field)
    best = PER_CATEGORY_BEST.get(cat, "E05")
    return vals.get(best, None)


def strategy_s2(field: str, vals: dict, weights: dict):
    cat = classify_field(field)
    best = PER_CATEGORY_BEST.get(cat, "E05")
    v = vals.get(best)
    if is_null_like(v):
        # Fall back to majority across remaining engines
        majority = majority_value(vals, threshold=3)
        if majority[0] == "majority":
            return majority[1][0]
        if majority[0] == "all_null":
            return None
        # No majority — keep null
        return None
    return v


def strategy_s3(field: str, vals: dict, weights: dict):
    majority = majority_value(vals, threshold=3)
    if majority[0] == "majority":
        return majority[1][0]
    if majority[0] == "all_null":
        return None
    cat = classify_field(field)
    best = PER_CATEGORY_BEST.get(cat, "E05")
    return vals.get(best, None)


def strategy_s4(field: str, vals: dict, weights: dict):
    cat = classify_field(field)
    return weighted_value(vals, cat, weights)


def strategy_s5(field: str, vals: dict, weights: dict):
    cat = classify_field(field)
    best = PER_CATEGORY_BEST.get(cat, "E05")
    best_v = vals.get(best)
    if is_null_like(best_v):
        # If any other engine ALSO says null, prefer null (avoid over-extraction)
        others_null = sum(1 for t, v in vals.items() if t != best and is_null_like(v))
        if others_null >= 1:
            return None
        # else fall back to weighted
        return weighted_value(vals, cat, weights)
    return weighted_value(vals, cat, weights)


def load_per_field_weights() -> dict:
    """Per-field per-engine accuracy from per-field-accuracy.csv."""
    weights: dict = {}
    with open(REPORT_DATA_DIR / "per-field-accuracy.csv") as f:
        reader = csv.DictReader(f)
        for row in reader:
            weights[row["field"]] = {tag: float(row[tag]) for tag, _ in ENGINES}
    return weights


PER_FIELD_WEIGHTS_CACHE: dict | None = None


def strategy_s6(field: str, vals: dict, weights: dict):
    """Per-field weighted majority — each engine weighted by its own per-field
    accuracy. Finer-grained than S4 (which uses per-category weights)."""
    global PER_FIELD_WEIGHTS_CACHE
    if PER_FIELD_WEIGHTS_CACHE is None:
        PER_FIELD_WEIGHTS_CACHE = load_per_field_weights()
    pfw = PER_FIELD_WEIGHTS_CACHE.get(field, {tag: 1.0 for tag, _ in ENGINES})
    canon = {tag: canonical_for_vote(v) for tag, v in vals.items()}
    scores: dict[str, float] = defaultdict(float)
    rep: dict[str, object] = {}
    for tag, c in canon.items():
        scores[c] += pfw.get(tag, 0.0)
        if c not in rep:
            rep[c] = vals[tag]
    best = max(scores.items(), key=lambda x: x[1])
    return None if best[0] == "__NULL__" else rep[best[0]]


def strategy_oracle(field: str, vals: dict, weights: dict, gt_value=None):
    """Cheating baseline: if any engine got it right, pick that engine's
    value. Returns the upper bound the ensemble could ever reach with
    perfect routing — useful for measuring headroom."""
    for v in vals.values():
        if field_matches(v, gt_value, field):
            return v
    # No engine got it right — fall back to weighted majority (S4)
    cat = classify_field(field)
    return weighted_value(vals, cat, weights)


STRATEGIES = {
    "S1_per_category_best": strategy_s1,
    "S2_best_then_majority_fallback": strategy_s2,
    "S3_majority_then_best": strategy_s3,
    "S4_weighted_majority": strategy_s4,
    "S5_weighted_with_null_preference": strategy_s5,
    "S6_per_field_weighted_majority": strategy_s6,
}


def evaluate_predictions(preds_by_sample: dict, sample_ids: list) -> dict:
    """Mirror of apps/temporal/src/evaluators/schema-aware-evaluator.ts'
    calculateMetrics in Python. A substitution (pred non-null, GT non-null,
    values differ) increments BOTH FP and FN; a deletion (pred null, GT
    non-null) increments FN only; an insertion (pred non-null, GT null)
    increments FP only; extra-key non-null predictions increment FP.
    """
    per_sample = {}
    for sid in sample_ids:
        gt = load_gt(sid)
        pred = preds_by_sample.get(sid, {})
        total_gt = len(gt)
        matched = 0
        false_negatives = 0
        false_positives = 0
        true_positives = 0
        details = []
        for field, expected in gt.items():
            predicted = pred.get(field)
            m = field_matches(predicted, expected, field)
            details.append({"field": field, "matched": m, "predicted": predicted, "expected": expected})
            if m:
                matched += 1
                true_positives += 1
                continue
            pred_null = is_null_like(predicted)
            exp_null = is_null_like(expected)
            if pred_null:
                false_negatives += 1            # deletion: FN only
            elif exp_null:
                false_positives += 1            # insertion: FP only
            else:
                false_positives += 1            # substitution: FP + FN
                false_negatives += 1
        for field, value in pred.items():
            if field not in gt and not is_null_like(value):
                false_positives += 1            # extra-key hallucination
        precision = (true_positives / (true_positives + false_positives)
                     if (true_positives + false_positives) > 0 else 0)
        recall = (true_positives / (true_positives + false_negatives)
                  if (true_positives + false_negatives) > 0 else 0)
        f1 = (2 * precision * recall / (precision + recall)
              if (precision + recall) > 0 else 0)
        per_sample[sid] = {
            "f1": f1, "precision": precision, "recall": recall,
            "matchedFields": matched, "totalGroundTruthFields": total_gt,
            "truePositives": true_positives, "falseNegatives": false_negatives,
            "falsePositives": false_positives,
            "pass": f1 >= 0.8,
            "details": details,
        }
    return per_sample


def aggregate(per_sample: dict) -> dict:
    import statistics
    n = len(per_sample)
    passing = sum(1 for s in per_sample.values() if s["pass"])
    f1s = [s["f1"] for s in per_sample.values()]
    precisions = [s["precision"] for s in per_sample.values()]
    recalls = [s["recall"] for s in per_sample.values()]
    matched = [s["matchedFields"] for s in per_sample.values()]
    fps = [s["falsePositives"] for s in per_sample.values()]
    return {
        "pass_rate": passing / n if n else 0,
        "passing_samples": passing,
        "total_samples": n,
        "f1.median": statistics.median(f1s),
        "f1.mean": statistics.mean(f1s),
        "f1.min": min(f1s),
        "f1.max": max(f1s),
        "precision.mean": statistics.mean(precisions),
        "recall.mean": statistics.mean(recalls),
        "matchedFields.median": statistics.median(matched),
        "falsePositives.mean": statistics.mean(fps),
    }


def per_category_accuracy(per_sample: dict) -> dict:
    counts = defaultdict(lambda: [0, 0])  # [correct, total]
    for s in per_sample.values():
        for d in s["details"]:
            cat = classify_field(d["field"])
            counts[cat][1] += 1
            if d["matched"]:
                counts[cat][0] += 1
    return {cat: (c[0] / c[1] if c[1] else 0) for cat, c in counts.items()}


def main() -> None:
    # Load engine predictions
    preds_by_engine = {tag: load_predictions(slug) for tag, slug in ENGINES}
    weights = load_per_category_weights()

    # Common set of sample IDs (intersection)
    sample_sets = [set(preds_by_engine[tag].keys()) for tag, _ in ENGINES]
    common = sorted(set.intersection(*sample_sets))
    print(f"Sample intersection: {len(common)} samples present in all {len(ENGINES)} engines.")
    if not common:
        print("No common samples — aborting.")
        sys.exit(1)

    # Build a global "fields seen" set from union of GT and all engines for each sample
    # (we use GT keys per-sample; engines may emit extras which become FPs)
    # First: baseline single-engine numbers (sanity check)
    baselines = {}
    for tag, _ in ENGINES:
        ps = evaluate_predictions(preds_by_engine[tag], common)
        baselines[tag] = aggregate(ps)

    print("\n== Baseline (re-evaluated against current GT, intersection of 40) ==")
    for tag, _ in ENGINES:
        m = baselines[tag]
        print(f"  {tag}  pass={m['pass_rate']:.3f}  f1.median={m['f1.median']:.3f}  "
              f"f1.mean={m['f1.mean']:.3f}  matched.median={int(m['matchedFields.median'])}  "
              f"fp.mean={m['falsePositives.mean']:.3f}")

    # Run each strategy
    strategy_results = {}
    strategy_per_sample = {}
    strategy_predictions = {}
    for name, fn in STRATEGIES.items():
        preds = {}
        for sid in common:
            gt = load_gt(sid)
            combined: dict = {}
            for field in gt.keys():  # ensemble produces only GT-keyed fields
                vals = {tag: preds_by_engine[tag][sid].get(field) for tag, _ in ENGINES}
                combined[field] = fn(field, vals, weights)
            preds[sid] = combined
        ps = evaluate_predictions(preds, common)
        strategy_results[name] = aggregate(ps)
        strategy_per_sample[name] = ps
        strategy_predictions[name] = preds

    # Oracle baseline (cheating upper bound)
    oracle_preds: dict = {}
    for sid in common:
        gt = load_gt(sid)
        combined: dict = {}
        for field, expected in gt.items():
            vals = {tag: preds_by_engine[tag][sid].get(field) for tag, _ in ENGINES}
            combined[field] = strategy_oracle(field, vals, weights, gt_value=expected)
        oracle_preds[sid] = combined
    ps_oracle = evaluate_predictions(oracle_preds, common)
    strategy_results["Z_oracle_upper_bound"] = aggregate(ps_oracle)
    strategy_per_sample["Z_oracle_upper_bound"] = ps_oracle
    strategy_predictions["Z_oracle_upper_bound"] = oracle_preds

    print("\n== Ensemble strategies ==")
    print(f"  {'strategy':38s}  pass    f1.med  f1.mean  matched.med  fp.mean")
    for name, m in strategy_results.items():
        print(f"  {name:38s}  {m['pass_rate']:.3f}   {m['f1.median']:.3f}   "
              f"{m['f1.mean']:.3f}   {int(m['matchedFields.median']):2d}           {m['falsePositives.mean']:.3f}")

    # Save the best non-oracle strategy's predictions and full results
    deployable = {k: v for k, v in strategy_results.items() if not k.startswith("Z_")}
    best_strategy = max(deployable.items(), key=lambda kv: (kv[1]["f1.mean"], kv[1]["pass_rate"], kv[1]["matchedFields.median"]))
    print(f"\n== Best strategy by (f1.mean, pass_rate, matched.median): {best_strategy[0]} ==")
    best_name = best_strategy[0]
    best_metrics = best_strategy[1]
    print(json.dumps(best_metrics, indent=2))

    # Write the best strategy's combined predictions for each sample
    out_preds = PRED_DIR / "best-strategy-predictions.json"
    with open(out_preds, "w") as f:
        json.dump({
            "strategy": best_name,
            "metrics": best_metrics,
            "predictions": strategy_predictions[best_name],
        }, f, indent=2)
    print(f"\n✓ wrote {out_preds}")

    # Build a synthesised benchmark-run.json so the comparison report can
    # include E06 alongside the others.
    per_sample = strategy_per_sample[best_name]
    eval_blob = []
    metric_keys = ["f1", "precision", "recall", "matchedFields", "totalGroundTruthFields",
                   "truePositives", "falseNegatives", "falsePositives"]
    per_sample_results = []
    for sid in common:
        s = per_sample[sid]
        per_sample_results.append({
            "sampleId": sid,
            "metadata": {},
            "metrics": {k: s[k] for k in metric_keys} | {"checkboxAccuracy": 0},
            "pass": s["pass"],
            "diagnostics": {
                "totalFields": s["totalGroundTruthFields"],
                "matchedFields": s["matchedFields"],
            },
            "groundTruth": load_gt(sid),
            "prediction": strategy_predictions[best_name][sid],
            "evaluationDetails": s["details"],
        })
    # Per-field aggregation
    field_counts = defaultdict(lambda: {"total": 0, "correct": 0, "errors": []})
    for sr in per_sample_results:
        for d in sr["evaluationDetails"]:
            entry = field_counts[d["field"]]
            entry["total"] += 1
            if d["matched"]:
                entry["correct"] += 1
            else:
                entry["errors"].append({"sampleId": sr["sampleId"], "predicted": d["predicted"], "expected": d["expected"]})
    per_field_results = []
    for field, c in field_counts.items():
        per_field_results.append({
            "field": field,
            "fieldType": "string",
            "totalCount": c["total"],
            "correctCount": c["correct"],
            "errorCount": c["total"] - c["correct"],
            "accuracy": c["correct"] / c["total"] if c["total"] else 0,
            "errorRate": 1 - (c["correct"] / c["total"] if c["total"] else 0),
            "errors": c["errors"],
        })
    per_field_results.sort(key=lambda x: -x["errorRate"])

    flat = {k: v for k, v in best_metrics.items()}
    flat["passing_samples"] = best_metrics["passing_samples"]
    flat["failing_samples"] = best_metrics["total_samples"] - best_metrics["passing_samples"]

import * as fs from "node:fs";
import * as os from "node:os";
import * as path from "node:path";
import type { BlobStorageInterface } from "@/blob-storage/blob-storage.interface";
import type {
  BlobFilePath,
  BlobPrefixPath,
} from "@/blob-storage/storage-path-builder";
import { syncLocalDatasetToBlobStorage } from "./local-dataset-sync.service";
import type { ParsedLocalDataset } from "./local-datasets";

interface FakeBlobStore {
  blobs: Map<string, Buffer>;
  storage: BlobStorageInterface;
  writes: Array<{ key: string; size: number }>;
}

function makeFakeBlobStorage(prePopulated: string[] = []): FakeBlobStore {
  const blobs = new Map<string, Buffer>();
  for (const key of prePopulated) {
    blobs.set(key, Buffer.from("seed"));
  }
  const writes: Array<{ key: string; size: number }> = [];
  const storage: BlobStorageInterface = {
    async write(key: BlobFilePath, data: Buffer): Promise<void> {
      blobs.set(key, data);
      writes.push({ key, size: data.length });
    },
    async read(key: BlobFilePath): Promise<Buffer> {
      const v = blobs.get(key);
      if (!v) throw new Error(`Not found: ${key}`);
      return v;
    },
    async exists(key: BlobFilePath): Promise<boolean> {
      return blobs.has(key);
    },
    async delete(key: BlobFilePath): Promise<void> {
      blobs.delete(key);
    },
    async list(_prefix: BlobPrefixPath): Promise<string[]> {
      return Array.from(blobs.keys());
    },
    async deleteByPrefix(_prefix: BlobPrefixPath): Promise<void> {
      // unused
    },
  };
  return { blobs, storage, writes };
}

interface DatasetVersionUpdateData {
  storagePrefix: string;
  manifestPath: string;
  documentCount: number;
}

function makePrismaStub() {
  const updates: Array<{
    where: { id: string };
    data: DatasetVersionUpdateData;
  }> = [];
  const client = {
    datasetVersion: {
      update: async (args: {
        where: { id: string };
        data: DatasetVersionUpdateData;
      }) => {
        updates.push(args);
        return null as unknown;
      },
    },
  };
  return { updates, client };
}

function setupRepoWithDataset(
  folder: string,
  visibility: "public" | "private",
  files: Array<{ name: string; content: string }>,
): {
  repoRoot: string;
  datasetsDir: string;
  cleanup: () => void;
  parsed: ParsedLocalDataset;
} {
  const repoRoot = fs.mkdtempSync(path.join(os.tmpdir(), "sync-test-"));
  const datasetsDir = path.join(repoRoot, "data", "datasets");
  const folderPath = path.join(datasetsDir, folder, visibility);
  fs.mkdirSync(folderPath, { recursive: true });
  for (const f of files) {
    fs.writeFileSync(path.join(folderPath, f.name), f.content);
  }

  // Build the manifest matching what parseLocalDatasets would produce.
  const docs = files
    .filter((f) => /\.(jpg|jpeg|png|pdf)$/i.test(f.name))
    .map((f) => ({ stem: f.name.replace(/\.[^.]+$/, ""), filename: f.name }));
  const manifest = {
    datasetName: folder,
    metadata: { autoGenerated: true },
    samples: docs.map((d) => ({
      id: d.stem,
      inputs: [
        {
          path: d.filename,
          mimeType: d.filename.toLowerCase().endsWith(".pdf")
            ? "application/pdf"
            : "image/jpeg",
        },
      ],
      groundTruth: [{ path: `${d.stem}.json`, format: "field-key-value-json" }],
    })),
  };
  fs.writeFileSync(
    path.join(folderPath, "manifest.json"),
    JSON.stringify(manifest, null, 2),
  );

  const parsed: ParsedLocalDataset = {
    folder,
    visibility,
    datasetName: folder,
    templateModelKey: null,
    sampleCount: manifest.samples.length,
    manifestPath: path.relative(
      repoRoot,
      path.join(folderPath, "manifest.json"),
    ),
    storagePrefix: path.relative(repoRoot, folderPath),
    autoGenerated: true,
  };

  return {
    repoRoot,
    datasetsDir,
    cleanup: () => fs.rmSync(repoRoot, { recursive: true, force: true }),
    parsed,
  };
}

describe("syncLocalDatasetToBlobStorage", () => {
  it("uploads input + ground-truth + dataset-manifest.json on first sync", async () => {
    const { repoRoot, parsed, cleanup } = setupRepoWithDataset(
      "samples-mix",
      "private",
      [
        { name: "form-001.jpg", content: "img1" },
        { name: "form-001.json", content: '{"name":"a"}' },
        { name: "form-002.jpg", content: "img2" },
        { name: "form-002.json", content: '{"name":"b"}' },
      ],
    );
    const blob = makeFakeBlobStorage();
    const prisma = makePrismaStub();
    try {
      const result = await syncLocalDatasetToBlobStorage(
        parsed,
        repoRoot,
        blob.storage,
        prisma.client,
      );
      expect(result.uploaded).toBe(4); // 2 inputs + 2 ground-truth
      expect(result.skipped).toBe(0);

      // Manifest written
      const manifestKeys = Array.from(blob.blobs.keys()).filter((k) =>
        k.endsWith("dataset-manifest.json"),
      );
      expect(manifestKeys).toHaveLength(1);

      const manifestContent = JSON.parse(
        blob.blobs.get(manifestKeys[0])!.toString("utf-8"),
      );
      expect(manifestContent.schemaVersion).toBe("1.0");
      expect(manifestContent.samples).toHaveLength(2);
      expect(manifestContent.samples[0].inputs[0].path).toBe(
        "inputs/form-001.jpg",
      );
      expect(manifestContent.samples[0].groundTruth[0].path).toBe(
        "ground-truth/form-001.json",
      );

      // Files at expected paths
      expect(
        Array.from(blob.blobs.keys()).filter((k) =>
          k.includes("/inputs/form-001.jpg"),
        ),
      ).toHaveLength(1);
      expect(
        Array.from(blob.blobs.keys()).filter((k) =>
          k.includes("/ground-truth/form-001.json"),
        ),
      ).toHaveLength(1);

      // DatasetVersion updated — manifestPath stored relative to storagePrefix
      // so the benchmark materializer's path.join(materializedPath, manifestPath)
      // resolves to the file that materializeDataset wrote at <materializedPath>/dataset-manifest.json.
      expect(prisma.updates).toHaveLength(1);
      expect(prisma.updates[0].where.id).toBe(
        "seed-local-samples-mix-private-v1",
      );
      expect(prisma.updates[0].data.manifestPath).toBe("dataset-manifest.json");
      expect(prisma.updates[0].data.storagePrefix).toBe(
        "datasets/seed-local-samples-mix-private/seed-local-samples-mix-private-v1",
      );
    } finally {
      cleanup();
    }
  });

  it("skips files that already exist in blob storage (idempotent re-sync)", async () => {
    const { repoRoot, parsed, cleanup } = setupRepoWithDataset(
      "ds",
      "private",
      [
        { name: "a.jpg", content: "img" },
        { name: "a.json", content: "{}" },
      ],
    );
    const blob = makeFakeBlobStorage();
    const prisma = makePrismaStub();
    try {
      // First sync uploads all
      const first = await syncLocalDatasetToBlobStorage(
        parsed,
        repoRoot,
        blob.storage,
        prisma.client,
      );
      expect(first.uploaded).toBe(2);

      const writeCountAfterFirst = blob.writes.length;

      // Second sync should skip the data files (manifest is always rewritten)
      const second = await syncLocalDatasetToBlobStorage(
        parsed,
        repoRoot,
        blob.storage,
        prisma.client,
      );
      expect(second.uploaded).toBe(0);
      expect(second.skipped).toBe(2);

      // Only manifest write happened on second sync
      const newWrites = blob.writes.length - writeCountAfterFirst;
      expect(newWrites).toBe(1);
      expect(blob.writes[blob.writes.length - 1].key).toMatch(
        /dataset-manifest\.json$/,
      );
    } finally {
      cleanup();
    }
  });

  it("uploads only the new files when sample list grows between runs", async () => {
    const { repoRoot, parsed, cleanup } = setupRepoWithDataset(
      "ds",
      "private",
      [
        { name: "a.jpg", content: "img" },
        { name: "a.json", content: "{}" },
      ],
    );
    const blob = makeFakeBlobStorage();
    const prisma = makePrismaStub();
    try {
      await syncLocalDatasetToBlobStorage(
        parsed,
        repoRoot,
        blob.storage,
        prisma.client,
      );

      // Add a new pair to the local folder + update manifest accordingly.
      const folderPath = path.join(repoRoot, parsed.storagePrefix);
      fs.writeFileSync(path.join(folderPath, "b.jpg"), "img2");
      fs.writeFileSync(path.join(folderPath, "b.json"), "{}");
      const manifest = JSON.parse(
        fs.readFileSync(path.join(repoRoot, parsed.manifestPath), "utf-8"),
      );
      manifest.samples.push({
        id: "b",
        inputs: [{ path: "b.jpg", mimeType: "image/jpeg" }],
        groundTruth: [{ path: "b.json", format: "field-key-value-json" }],
      });
      fs.writeFileSync(
        path.join(repoRoot, parsed.manifestPath),
        JSON.stringify(manifest),
      );
      const updatedParsed = { ...parsed, sampleCount: 2 };

      const second = await syncLocalDatasetToBlobStorage(
        updatedParsed,
        repoRoot,
        blob.storage,
        prisma.client,
      );
      expect(second.uploaded).toBe(2); // b.jpg + b.json
      expect(second.skipped).toBe(2); // a.jpg + a.json
    } finally {
      cleanup();
    }
  });

  it("blob keys follow the existing benchmark convention", async () => {
    const { repoRoot, parsed, cleanup } = setupRepoWithDataset(
      "samples-mix",
      "private",
      [
        { name: "x.jpg", content: "img" },
        { name: "x.json", content: "{}" },
      ],
    );
    const blob = makeFakeBlobStorage();
    const prisma = makePrismaStub();
    try {
      await syncLocalDatasetToBlobStorage(
        parsed,
        repoRoot,
        blob.storage,
        prisma.client,
      );

      const keys = Array.from(blob.blobs.keys());
      // Existing convention used by dataset.service uploadFiles:
      //   groups/{groupId}/benchmark/datasets/{datasetId}/{versionId}/{inputs|ground-truth}/{filename}
      const inputKey = keys.find((k) => k.endsWith("/inputs/x.jpg"));
      const gtKey = keys.find((k) => k.endsWith("/ground-truth/x.json"));
      const manifestKey = keys.find((k) =>
        k.endsWith("/dataset-manifest.json"),
      );
      expect(inputKey).toBeDefined();
      expect(gtKey).toBeDefined();
      expect(manifestKey).toBeDefined();
      expect(inputKey).toContain("seeddefaultgroup/benchmark/");
      expect(inputKey).toContain(
        "datasets/seed-local-samples-mix-private/seed-local-samples-mix-private-v1/inputs/",
      );
    } finally {
      cleanup();
    }
  });
});

/**
 * Helpers for the local-folder dataset seed convention.
 *
 * Convention: `data/datasets/<name>/{public,private}/`. Two layouts are accepted:
 *
 *  1. Manifest-driven: a `manifest.json` describes samples, with optional
 *     `documents/` and `ground-truth/` subfolders. Authoritative when the file
 *     is curated by the user. See
 *     `docs/superpowers/specs/2026-05-08-extraction-experiments-design.md`.
 *
 *  2. Flat-pair: drop `<name>.<image-ext>` + `<name>.json` pairs directly into
 *     the visibility folder. The seed auto-derives a manifest from the pairs
 *     and writes it as `manifest.json` with `metadata.autoGenerated = true`,
 *     so subsequent re-seeds refresh it. If the user removes the auto-gen
 *     marker (or curates their own), it's treated as user-managed and never
 *     regenerated.
 *
 * The parser is a pure function so it can be unit-tested without a database
 * or filesystem mock. The seed in `seed.ts` calls it and persists the records.
 */

import * as fs from "node:fs";
import * as path from "node:path";

export type DatasetVisibility = "public" | "private";

export interface LocalDatasetSampleManifest {
  id: string;
  inputs?: Array<{ path: string; mimeType: string }>;
  groundTruth?: Array<{ path: string; format: string }>;
  metadata?: Record<string, unknown>;
}

export interface LocalDatasetManifest {
  datasetName?: string;
  templateModelKey?: string;
  metadata?: Record<string, unknown>;
  samples?: LocalDatasetSampleManifest[];
}

export interface ParsedLocalDataset {
  /** Folder name under `data/datasets/`. */
  folder: string;
  /** Public or private subfolder. */
  visibility: DatasetVisibility;
  /** Display name from manifest.datasetName, falls back to folder. */
  datasetName: string;
  /** Optional `templateModelKey` from manifest. */
  templateModelKey: string | null;
  /** Number of samples in `manifest.samples`. */
  sampleCount: number;
  /** Repo-relative path to manifest.json. */
  manifestPath: string;
  /** Repo-relative path to the directory containing the manifest. */
  storagePrefix: string;
  /** True when this manifest was generated from flat pairs and may be re-generated. */
  autoGenerated: boolean;
}

export interface ParseLocalDatasetsLogger {
  warn(message: string): void;
}

/** Image / document file extensions accepted for flat-pair mode (lowercase). */
const FLAT_PAIR_DOC_EXTENSIONS = new Set([
  ".jpg",
  ".jpeg",
  ".png",
  ".pdf",
  ".tif",
  ".tiff",
  ".webp",
  ".bmp",
  ".gif",
]);

const MIME_BY_EXT: Record<string, string> = {
  ".jpg": "image/jpeg",
  ".jpeg": "image/jpeg",
  ".png": "image/png",
  ".pdf": "application/pdf",
  ".tif": "image/tiff",
  ".tiff": "image/tiff",
  ".webp": "image/webp",
  ".bmp": "image/bmp",
  ".gif": "image/gif",
};

/**
 * Build a manifest by pairing `<name>.<doc-ext>` with `<name>.json` in the
 * folder. Returns null if no pairs are found. Files without a matching pair
 * (e.g. orphan jpg or json) are reported via the logger and skipped.
 */
export function deriveManifestFromFlatPairs(
  folderPath: string,
  folder: string,
  log: ParseLocalDatasetsLogger,
): LocalDatasetManifest | null {
  if (!fs.existsSync(folderPath) || !fs.statSync(folderPath).isDirectory()) {
    return null;
  }

  const entries = fs.readdirSync(folderPath, { withFileTypes: true });
  const docByStem = new Map<string, string>(); // stem -> filename
  const jsonStems = new Set<string>();

  for (const entry of entries) {
    if (!entry.isFile()) continue;
    const ext = path.extname(entry.name).toLowerCase();
    const stem = entry.name.slice(0, entry.name.length - ext.length);
    if (FLAT_PAIR_DOC_EXTENSIONS.has(ext)) {
      docByStem.set(stem, entry.name);
    } else if (ext === ".json" && entry.name !== "manifest.json") {
      jsonStems.add(stem);
    }
  }

  if (docByStem.size === 0 && jsonStems.size === 0) {
    return null;
  }

  const samples: LocalDatasetSampleManifest[] = [];
  const stems = Array.from(docByStem.keys()).sort((a, b) => a.localeCompare(b));
  for (const stem of stems) {
    const docFilename = docByStem.get(stem);
    if (!docFilename) continue;
    if (!jsonStems.has(stem)) {
      log.warn(
        `Skipping unpaired document in ${folder}: '${docFilename}' has no matching '${stem}.json'`,
      );
      continue;
    }
    const ext = path.extname(docFilename).toLowerCase();
    samples.push({
      id: stem,
      inputs: [
        {
          path: docFilename,
          mimeType: MIME_BY_EXT[ext] ?? "application/octet-stream",
        },
      ],
      groundTruth: [{ path: `${stem}.json`, format: "field-key-value-json" }],
    });
  }

  for (const stem of jsonStems) {
    if (!docByStem.has(stem)) {
      log.warn(
        `Skipping unpaired ground-truth in ${folder}: '${stem}.json' has no matching document`,
      );
    }
  }

  if (samples.length === 0) {
    return null;
  }

  return {
    datasetName: folder,
    metadata: { autoGenerated: true },
    samples,
  };
}

/**
 * Scan `<datasetsDir>` for `<name>/{public,private}/` folders. For each:
 *
 *   1. If `manifest.json` exists:
 *      - if `metadata.autoGenerated === true`, regenerate from flat pairs
 *        (so re-seeds pick up file changes).
 *      - otherwise treat as user-managed: parse and use as-is.
 *   2. If no `manifest.json`, attempt to derive one from flat `<name>.<ext>` +
 *      `<name>.json` pairs. If derived, write it as `manifest.json` so the
 *      runtime / benchmark service can read it.
 *
 * `repoRoot` is used to make `manifestPath` and `storagePrefix` repo-relative
 * (the convention used by existing `DatasetVersion` rows).
 */
export function parseLocalDatasets(
  datasetsDir: string,
  repoRoot: string,
  log: ParseLocalDatasetsLogger,
): ParsedLocalDataset[] {
  if (!fs.existsSync(datasetsDir) || !fs.statSync(datasetsDir).isDirectory()) {
    return [];
  }

  const folders = fs
    .readdirSync(datasetsDir, { withFileTypes: true })
    .filter((e) => e.isDirectory())
    .map((e) => e.name);

  const results: ParsedLocalDataset[] = [];
  for (const folder of folders) {
    for (const visibility of ["public", "private"] as const) {
      const folderPath = path.join(datasetsDir, folder, visibility);
      const manifestFile = path.join(folderPath, "manifest.json");

      if (!fs.existsSync(folderPath)) continue;

      let manifest: LocalDatasetManifest | null = null;
      let autoGenerated = false;

      if (fs.existsSync(manifestFile)) {
        let parsed: LocalDatasetManifest;
        try {
          parsed = JSON.parse(fs.readFileSync(manifestFile, "utf-8"));
        } catch (err) {
          log.warn(
            `Skipping ${folder}/${visibility}: invalid manifest.json (${(err as Error).message})`,
          );
          continue;
        }
        const wasAutoGen =
          typeof parsed.metadata === "object" &&
          parsed.metadata !== null &&
          (parsed.metadata as Record<string, unknown>).autoGenerated === true;

        if (wasAutoGen) {
          // Re-derive from current files; user has not curated this manifest.
          const fresh = deriveManifestFromFlatPairs(folderPath, folder, log);
          if (fresh) {
            manifest = fresh;
            autoGenerated = true;
            fs.writeFileSync(manifestFile, JSON.stringify(fresh, null, 2));
          } else {
            log.warn(
              `Skipping ${folder}/${visibility}: previously auto-generated manifest, but no flat pairs found now`,
            );
            continue;
          }
        } else {
          if (!Array.isArray(parsed.samples)) {
            log.warn(
              `Skipping ${folder}/${visibility}: manifest.json is missing 'samples' array`,
            );
            continue;
          }
          manifest = parsed;
        }
      } else {
        const derived = deriveManifestFromFlatPairs(folderPath, folder, log);
        if (!derived) {
          continue;
        }
        manifest = derived;
        autoGenerated = true;
        fs.writeFileSync(manifestFile, JSON.stringify(derived, null, 2));
      }

      const samples = Array.isArray(manifest.samples) ? manifest.samples : [];
      results.push({
        folder,
        visibility,
        datasetName: manifest.datasetName ?? folder,
        templateModelKey: manifest.templateModelKey ?? null,
        sampleCount: samples.length,
        manifestPath: path.relative(repoRoot, manifestFile),
        storagePrefix: path.relative(repoRoot, folderPath),
        autoGenerated,
      });
    }
  }

  return results;
}

/** Stable Dataset row id derived from folder + visibility. */
export function localDatasetId(
  folder: string,
  visibility: DatasetVisibility,
): string {
  return `seed-local-${folder}-${visibility}`;
}

/** Stable DatasetVersion row id. */
export function localDatasetVersionId(
  folder: string,
  visibility: DatasetVisibility,
): string {
  return `${localDatasetId(folder, visibility)}-v1`;
}
